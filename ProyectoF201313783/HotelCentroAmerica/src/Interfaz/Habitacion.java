package Interfaz;

public class Habitacion {
	int numeroHabitacion, edificio, nivel, camas, precio;
	String sucursal, tipo;
	
	public Habitacion(String tipo){
		switch (tipo){
		case "Sencilla":
			precio = 200;
			break;
		case "Doble":
			precio = 250;
			break;
		case "Suite Delux":
			precio = 379;
			break;
		}
	}
	
}
